#ifndef MOVINGAVERAGEFILTER_H
#define MOVINGAVERAGEFILTER_H

#include "filter.h"

#include <memory>
#include <Eigen/Core>

template <class E> class MovingAverageFilter : public Filter<E>
{
public:
    MovingAverageFilter(int n = 10);
    ~MovingAverageFilter(){free(values);}
    void addMeasurement(E newMeasurement);
    E getValue(E newValue);
    inline E getValue(){return lastAverage;}
    void initialize(E value);
private:
    int head;
    int tail;
    int N;
    E * values;
    E lastAverage;
};

#endif // MOVINGAVERAGEFILTER_H
